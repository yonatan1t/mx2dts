"""Tests for the DTS writer (board and overlay modes)."""

from __future__ import annotations

import textwrap
import pytest

from mx2dts.dts_writer import generate_dts
from tests.conftest import make_ioc, make_ctx, MINIMAL_IOC


def _ctx(tmp_path, extra_ioc=""):
    full = MINIMAL_IOC + extra_ioc
    ioc_path = tmp_path / "board.ioc"
    ioc_path.write_text(textwrap.dedent(full))
    from mx2dts.ioc_parser import parse_ioc
    ioc = parse_ioc(ioc_path)
    return make_ctx(ioc, tmp_path)


# ── Board mode ────────────────────────────────────────────────────────────────

def test_board_mode_has_dts_version(tmp_path):
    ctx = _ctx(tmp_path)
    out = generate_dts(ctx, mode="board")
    assert "/dts-v1/;" in out


def test_board_mode_has_root_node(tmp_path):
    ctx = _ctx(tmp_path)
    out = generate_dts(ctx, mode="board")
    assert "model =" in out
    assert "compatible =" in out
    assert "zephyr,sram" in out
    assert "zephyr,flash" in out


def test_board_mode_has_console_chosen(tmp_path):
    ctx = _ctx(tmp_path)
    out = generate_dts(ctx, mode="board")
    assert "zephyr,console" in out


def test_board_mode_has_usart2(tmp_path):
    ctx = _ctx(tmp_path)
    out = generate_dts(ctx, mode="board")
    assert "&usart2" in out
    assert 'current-speed = <115200>' in out


def test_board_mode_flash_partitions(tmp_path):
    ctx = _ctx(tmp_path)
    out = generate_dts(ctx, mode="board")
    # STM32L476RGTx has 1MB flash (flash code G)
    assert "flash0" in out
    assert "storage_partition" in out


def test_board_mode_custom_board_name(tmp_path):
    ctx = _ctx(tmp_path)
    out = generate_dts(ctx, board_name="My Custom Board", mode="board")
    assert "My Custom Board" in out


