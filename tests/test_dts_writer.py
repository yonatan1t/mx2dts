# ── Warning rendering ─────────────────────────────────────────────────────────

def test_warnings_appear_in_output(tmp_path):
    ctx = _ctx(tmp_path)
    ctx.warn("test warning message")
    out = generate_dts(ctx, mode="board")
    assert "test warning message" in out
    assert "Conversion warnings" in out


def test_no_warnings_section_when_clean(tmp_path):
    """Warnings section only appears when ctx.warnings is non-empty."""
    from mx2dts.dts_writer import _render_warnings
    from mx2dts.context import ConversionContext
    from unittest.mock import MagicMock

    ctx = MagicMock(spec=ConversionContext)
    ctx.warnings = []
    assert _render_warnings(ctx) == []


# ── DtsNode rendering ─────────────────────────────────────────────────────────

def test_dts_node_render_basic():
    from mx2dts.converters import DtsNode
    node = DtsNode(ref="&usart2", properties={"status": '"okay"', "current-speed": "<115200>"})
    text = node.render()
    assert text.startswith("&usart2 {")
    assert 'status = "okay";' in text
    assert "current-speed = <115200>;" in text
    assert text.strip().endswith("};")


def test_dts_node_render_with_children():
    from mx2dts.converters import DtsNode
    child = DtsNode(ref="led0: led0", properties={"gpios": "<&gpioa 5 GPIO_ACTIVE_HIGH>"})
    parent = DtsNode(ref="leds", properties={"compatible": '"gpio-leds"'}, children=[child])
    text = parent.render()
    assert "leds {" in text
    assert "led0: led0 {" in text
    assert "gpios" in text


def test_dts_node_render_with_comment():
    from mx2dts.converters import DtsNode
    node = DtsNode(ref="&mac", properties={"status": '"okay"'}, comment="Verify PHY settings")
    text = node.render()
    assert "/* Verify PHY settings */" in text
